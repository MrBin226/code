function S = transform(pop1)
%将基因pop1转换为实际系统开关S
%基于环网的编码策略，公共支路只需要放在其中1个环网中，开关1不需要编码（始终闭合）
loop1=[2,3,4,5,18,19,20,33]; %8个开关
loop2=[22,23,24,25,26,27,28,37]; %8个开关
loop3=[8,9,10,11,21,35];%6个开关
loop4=[6,7,15,16,17,29,30,31,32,36];%10个开关
loop5=[12,13,14,34];%4个开关
S=zeros(1,5);   %S存放5个断开的开关编号
L1=pop1(1,1:8);
L2=pop1(1,9:16);
L3=pop1(1,17:22);
L4=pop1(1,23:32);
L5=pop1(1,33:36);
S(1,1)=loop1(L1==0);
S(1,2)=loop2(L2==0);
S(1,3)=loop3(L3==0);
S(1,4)=loop4(L4==0);
S(1,5)=loop5(L5==0);
end
